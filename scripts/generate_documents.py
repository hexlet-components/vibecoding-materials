"""
Генератор учебного набора документов для урока про Claude Cowork.

Создаёт папку documents/ с ~15 PDF, имитирующую реальный бардак в папке
небольшого ИП за квартал: счета, чеки, договоры, выписки, акт плюс пара
посторонних файлов. Часть имён файлов бессмысленная (scan_001.pdf,
IMG_20260228.pdf), даты документов зашиты внутрь PDF, а не в имя. Среди
файлов есть два настоящих дубликата под разными именами и один похожий, но
не дубликат (счёт за тот же хостинг, но за другой месяц) — ловушка.

Запуск:  .venv/bin/python scripts/generate_documents.py
"""

from pathlib import Path
from fpdf import FPDF
from fpdf.enums import XPos, YPos

FONT = "/System/Library/Fonts/Supplemental/Arial.ttf"
FONT_BOLD = "/System/Library/Fonts/Supplemental/Arial Bold.ttf"
OUT = Path(__file__).resolve().parent.parent / "документы-ип"


def doc(filename, title, lines):
    """lines — список строк или кортежей ('заголовок раздела', None) для подзаголовка."""
    pdf = FPDF(format="A4")
    pdf.add_font("Arial", "", FONT)
    pdf.add_font("Arial", "B", FONT_BOLD)
    pdf.set_auto_page_break(True, margin=20)
    pdf.add_page()

    nl = dict(new_x=XPos.LMARGIN, new_y=YPos.NEXT)

    pdf.set_font("Arial", "B", 16)
    pdf.multi_cell(0, 9, title, **nl)
    pdf.ln(4)

    pdf.set_font("Arial", "", 12)
    for line in lines:
        if isinstance(line, tuple):
            pdf.ln(2)
            pdf.set_font("Arial", "B", 12)
            pdf.multi_cell(0, 7, line[0], **nl)
            pdf.set_font("Arial", "", 12)
        elif line == "":
            pdf.ln(3)
        else:
            pdf.multi_cell(0, 7, line, **nl)

    OUT.mkdir(parents=True, exist_ok=True)
    pdf.output(str(OUT / filename))
    print("✓", filename)


